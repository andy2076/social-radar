function seededRandom(seed: number): () => number {
  let s = seed;
  return () => {
    s = (s * 16807 + 0) % 2147483647;
    return s / 2147483647;
  };
}

export function generateMockData(): Post[] {
  const random = seededRandom(42);
  const posts: Post[] = [];
  const startDate = new Date("2025-01-20");

  for (let i = 0; i < 200; i++) {
    const dayOffset = Math.floor(random() * 30);
    const date = new Date(startDate);
    date.setDate(date.getDate() + dayOffset);

    const accountIndex = Math.floor(random() * ACCOUNT_NAMES.length);
    const topicIndex = Math.floor(random() * TOPICS.length);
    const topic = TOPICS[topicIndex];
    const templates = CONTENT_TEMPLATES[topic];
    const contentIndex = Math.floor(random() * templates.length);

    posts.push({
      id: `mock-${i + 1}`,
      date: date.toISOString().split("T")[0],
      accountId: `account-${accountIndex + 1}`,
      accountName: ACCOUNT_NAMES[accountIndex],
      topic,
      content: templates[contentIndex],
      likes: Math.floor(random() * 500),
      retweets: Math.floor(random() * 200),
      replies: Math.floor(random() * 100),
      impressions: Math.floor(random() * 50000),
      source: "mock",
    });
  }

  return posts.sort((a, b) => a.date.localeCompare(b.date));
}

